#Arithmetic Operators
print(16//5,1%5,2**0.5,16/5)
#Assignment Operators
x=12
y=45
x+=3
y%=6
print(x,y)
#Comparison Operators
i=6
print(i==5,i!=5)
#Logical Operators
a=True
b=False
print(a and a,a and b,a or b,b or b)
#Identity Operators
print(5 is 5, 5 is not 6,5 is not 5)
#Membership Operators
list1=[1,2,3,4]
print(1 in list1,9 in list1,4 not in list1 )
#Bitwise Operators
print(0 & 0,0 & 1,1 & 3,0 | 0, 0 | 1, 1 | 3)